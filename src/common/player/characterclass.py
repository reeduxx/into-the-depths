from enum import Enum

"""
Berserker (Strength focused)
Ranger (Dexterity focused)
Paladin (Constitution focused)
Warlock (Intelligence focused)
Rogue (Wisdom focused)
Merchant (Charisma focused)
"""

class CharacterClass(Enum):
    BERSERKER = 'berserker'
    RANGER = 'ranger'
    PALADIN = 'paladin'
    WARLOCK = 'warlock'
    ROGUE = 'rogue'
    MERCHANT = 'merchant'
    


